class ChatInput(BaseModel):
    """Input model for chat endpoint.

    Attributes:
        session_id: Unique identifier for the chat session.
        query: User's input query.
        always_search: Flag to force searching online instead of using cache.
    """
    session_id: str
    query: str
    always_search: bool = False # if set to True then use cached page parses stored in DB


class ChatOutput(BaseModel):
    """Output model for chat endpoint.

    Attributes:
        info: Information about the query processing.
        query_answer: Answer generated by the LLM.
        standalone_query: Rewritten query for clarity.
        relevant_urls: List of URLs relevant to the query.
        extracted_content: Preview of extracted content from relevant_urls.
        reference_urls: Set of URLs used as references. Subset of relevant_urls, retrieved from URLs of relevant_chunks.
        relevant_chunks: List of relevant text chunks extracted.
        status: Status of the query processing.
    """
    info: str
    query_answer: str = ""
    standalone_query: str = ""
    selected_domain: str = ""
    relevant_urls: list[str] = []
    extracted_content: list[dict] = []
    reference_urls: set[str] = set()
    relevant_chunks: list[str] = []
    status: str = "success"


